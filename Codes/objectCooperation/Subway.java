package objectCooperation;

public class Subway {
	
	int subNum;
	int riderSub;
	int incomeSub;
	
	public Subway() {
		this.subNum=subNum;
	}
	
	public void take(int subNum) {
		this.incomeSub=incomeSub;
		riderSub++;
	}
	
	public void showInfo() {
		System.out.println(subNum+" 라인 지하철의 승객은 "+riderSub+"명이고 수입은 "+incomeSub+"입니다.");
	}
	
	

}
